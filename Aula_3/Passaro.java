class Passaro {
  private String cor;
  private int tamanhoAsa;

  public String getCor() {
    return cor;
  }

  public int getTamanhoAsa() {
    return tamanhoAsa;
  }

  public void setCor(String newCor) {
    cor = newCor;
  }

  public void setTamanho(int newTamanhoAsa) {
    tamanhoAsa = newTamanhoAsa;
  }
}